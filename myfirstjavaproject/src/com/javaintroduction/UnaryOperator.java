package com.javaintroduction;

public class UnaryOperator {

	public static void main(String[] args) 
	{
		int a=10;
		int b=20;
		System.out.println(a++);
		System.out.println(--a);
		System.out.println(++a);
		System.out.println(a--);
		System.out.println(b--);
		System.out.println(--b);
		System.out.println(b++);
		System.out.println(++b);
		System.out.println(--b);
		// final values become
		System.out.println(a);
		System.out.println(b);
		

	}

}
