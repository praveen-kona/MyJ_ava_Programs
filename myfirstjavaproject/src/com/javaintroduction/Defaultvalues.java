package com.javaintroduction;

public class Defaultvalues {
	byte b;
	short s;
	int i;
	 long l;
	 float f;
	 double d;
	 char c;
	  boolean bool;

	public static void main(String[] args) {

		Defaultvalues d1=new Defaultvalues();
		System.out.println("byte value by default :"+d1.b);
		System.out.println("short value by default :"+d1.s);
		System.out.println("int value by default :"+d1.i);
		System.out.println("long value by default :"+d1.l);
		System.out.println("float value by default :"+d1.f);
		System.out.println("double value by default :"+d1.d);
		System.out.println("char value by default :"+d1.c);
		System.out.println("boolean value by default :"+d1.bool);
		
		
	}

}
